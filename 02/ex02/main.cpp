#include "Fixed.hpp"

int main( void ) {
	Fixed a;
	Fixed const b( Fixed( 5.05f ) * Fixed( 2 ) );
	std::cout << a << std::endl;
	std::cout << ++a << std::endl;
	std::cout << a << std::endl;
	std::cout << a++ << std::endl;
	std::cout << a << std::endl;
	std::cout << b / 0 << std::endl;
	std::cout << Fixed::max( a, b ) << std::endl;
	a = 8;
	std::cout << a << std::endl;
	return 0;
}