#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list	*prev;

	if (!lst || !f)
		return ;
	prev = lst;
	while (prev)
	{
		f(prev->content);
		prev = prev->next;
	}
}
